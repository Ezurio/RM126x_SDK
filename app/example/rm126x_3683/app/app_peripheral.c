// Public functions
void app_peripheral_init(peripheral_descriptor_t *peripheral_descriptor)
{
  char default_uplink_count_text[LENGTH_UPLINK_COUNT] = {0};
  sl_status_t init_result;

  /* Initialise the EInk Display */
  glib_init(&g_context);
  g_context.wrap = false;
  g_context.text_color = E_PAPER_SCREEN_COLOR_BLACK;

  init_result = mikroe_e_paper_154_inch_init(sl_spidrv_mikroe_handle);

  if (!init_result)
  {
    (void)mikroe_e_paper_154_inch_start_config();
    (void)mikroe_e_paper_154_inch_set_lut(EINK154_LUT_TABLE, 30);

    glib_fill(&g_context, E_PAPER_SCREEN_COLOR_WHITE);

    glib_draw_xbitmap(&g_context,
                      0, 0, ezurio_logo,
                      WIDTH_EZURIO_LOGO, HEIGHT_EZURIO_LOGO,
                      EINK154_SCREEN_COLOR_BLACK);

    glib_draw_xbitmap(&g_context,
                      0, HEIGHT_EZURIO_LOGO, rm126x_logo,
                      WIDTH_RM126X_TEXT, HEIGHT_RM126X_TEXT,
                      EINK154_SCREEN_COLOR_BLACK);

    glib_draw_string(&g_context, sdk_header_text,
                     (MIKROE_E_PAPER_WIDTH - strlen(sdk_header_text) *
                     WIDTH_CHAR + 1) / 2, X_HEADER_TEXT);

    app_peripheral_format_uplink_count(default_uplink_count_text, 0);

    glib_draw_string(&g_context, default_uplink_count_text,
                     (MIKROE_E_PAPER_WIDTH - strlen(default_uplink_count_text) *
                     WIDTH_CHAR + 1) / 2, X_UPLINK_TEXT);

    glib_update_display();
  }
  /* If initialisation failed, log and blank descriptor */
  if (init_result)
  {
    app_log("\n 3683 Initialisation failed.\n");
    peripheral_descriptor->app_peripheral_set = NULL;
  }
  else
  {
    peripheral_descriptor->app_peripheral_set = app_peripheral_set;
  }
  /* No get behaviour for 3683 boards */
  peripheral_descriptor->app_peripheral_get = NULL;
}

void app_peripheral_update(void)
{
  /* No update activity for 3683 based peripherals */
}

// -----------------------------------------------------------------------------
// Private functions

static void app_peripheral_set(void *in_data, uint8_t in_length)
{
  char updated_uplink_count_text[LENGTH_UPLINK_COUNT] = {0};
  uint16_t uplink_count = 0;

  ARG_UNUSED(in_length);

  /* Flip uplink count back to little endian format */
  uplink_count |= (uint16_t)((((uint8_t *)in_data)[0]) << 8);
  uplink_count |= (uint16_t)((((uint8_t *)in_data)[1]));

  app_peripheral_format_uplink_count(updated_uplink_count_text, uplink_count);

  glib_draw_string(&g_context, updated_uplink_count_text,
                   (MIKROE_E_PAPER_WIDTH - strlen(updated_uplink_count_text) *
                       WIDTH_CHAR + 1) / 2, X_UPLINK_TEXT);

  glib_update_display();
}

static void app_peripheral_format_uplink_count(char *out_string,
                                               uint16_t uplink_count)
{
  /* Uplink count is a uint16_t - we always want it to appear as 5 digits. */
  strcpy(out_string, uplink_count_text);
  sprintf(&out_string[strlen(out_string)], "%05"PRIu16, uplink_count);
}
